# ===================================================================
#  PDDL-Grounded World Model
# ===================================================================


# GroundedAction from bt_causal is used as the action info container.
# (Replaces the former GroundedActionInfo duplicate class.)


class SimulatedWorld:
    """PDDL-grounded stochastic world state.

    Maintains a set of positive fluents (closed-world assumption) and
    applies grounded actions by randomly selecting a non-deterministic
    outcome branch.

    Parameters
    ----------
    initial_fluents : set of str
        PR2-format fluent strings for the initial state.
    action_table : dict
        Mapping from action name → ``GroundedAction``.
    goal_pos, goal_neg : frozenset of str
        Positive / negative goal fluents.
    """

    def __init__(
        self,
        initial_fluents: Set[str],
        action_table: Dict[str, GroundedAction],
        goal_pos: FrozenSet[str],
        goal_neg: FrozenSet[str],
    ):
        self.fluents: Set[str] = set(initial_fluents)
        self.action_table = action_table
        self.goal_pos = goal_pos
        self.goal_neg = goal_neg
        self.goal_reached: bool = False

    # -- query ---------------------------------------------------------

    def holds(self, literal: str) -> bool:
        """Check whether *literal* holds under closed-world assumption."""
        if literal.startswith("not(") and literal.endswith(")"):
            return literal[4:-1] not in self.fluents
        return literal in self.fluents

    def check_goal(self) -> bool:
        """Return ``True`` when the current state satisfies the goal."""
        return all(f in self.fluents for f in self.goal_pos) and all(
            f not in self.fluents for f in self.goal_neg
        )

    # -- mutation ------------------------------------------------------

    def execute_action(self, action_str: str) -> bool:
        """Apply *action_str* with a uniformly random outcome.

        Returns ``True`` on success, ``False`` if the action is unknown
        or preconditions are not met.
        """
        info = self.action_table.get(action_str)
        if info is None:
            return False

        # Verify preconditions
        for pre in info.preconditions:
            if pre not in self.fluents:
                return False
        for neg_pre in info.neg_preconditions:
            if neg_pre in self.fluents:
                return False

        # Random outcome
        outcome_idx = random.randint(0, len(info.outcomes) - 1)
        add_set, del_set = info.outcomes[outcome_idx]

        # Apply: delete first, then add
        self.fluents -= del_set
        self.fluents |= add_set
        return True


# ===================================================================
#  py_trees Behaviour Nodes
# ===================================================================

# Blackboard key used by all simulator behaviours to access the world.
_WORLD_KEY = "world"


class FluentCondition(py_trees.behaviour.Behaviour):
    """Succeeds iff a fluent holds in the simulated world.

    Reads the ``SimulatedWorld`` from the py_trees blackboard.
    """

    def __init__(self, fluent: str, name: str = ""):
        super().__init__(name=name or fluent)
        self.fluent = fluent
        self.bb = self.attach_blackboard_client()
        self.bb.register_key(
            key=_WORLD_KEY, access=py_trees.common.Access.READ
        )

    def update(self) -> py_trees.common.Status:
        world: SimulatedWorld = self.bb.get(_WORLD_KEY)
        if world.holds(self.fluent):
            return py_trees.common.Status.SUCCESS
        return py_trees.common.Status.FAILURE


class ExecuteAction(py_trees.behaviour.Behaviour):
    """Executes a grounded action in the simulated world.

    On ``update()``, calls ``world.execute_action(action_name)``
    (which picks a random non-deterministic outcome).
    """

    def __init__(self, action_name: str, name: str = ""):
        super().__init__(name=name or f"Do:{action_name}")
        self.action_name = action_name
        self.bb = self.attach_blackboard_client()
        self.bb.register_key(
            key=_WORLD_KEY, access=py_trees.common.Access.WRITE
        )

    def update(self) -> py_trees.common.Status:
        world: SimulatedWorld = self.bb.get(_WORLD_KEY)
        if world.execute_action(self.action_name):
            return py_trees.common.Status.SUCCESS
        return py_trees.common.Status.FAILURE


class GoalReached(py_trees.behaviour.Behaviour):
    """Marks the goal as reached and succeeds."""

    def __init__(self):
        super().__init__(name="GoalReached")
        self.bb = self.attach_blackboard_client()
        self.bb.register_key(
            key=_WORLD_KEY, access=py_trees.common.Access.WRITE
        )

    def update(self) -> py_trees.common.Status:
        self.bb.get(_WORLD_KEY).goal_reached = True
        return py_trees.common.Status.SUCCESS


class AlwaysFailure(py_trees.behaviour.Behaviour):
    """Always returns FAILURE (used for forbidden / dead-end nodes)."""

    def __init__(self, name: str = "Fail"):
        super().__init__(name=name)

    def update(self) -> py_trees.common.Status:
        return py_trees.common.Status.FAILURE


# ===================================================================
#  Converter: pr2_to_bt.BTNode → py_trees
# ===================================================================


def convert_to_pytrees(node: BTNode) -> py_trees.behaviour.Behaviour:
    """Recursively convert a ``pr2_to_bt`` BT into a ``py_trees`` tree.

    Node mapping
    ------------
    ==================== ==========================================
    pr2_to_bt            py_trees
    ==================== ==========================================
    ReactiveSelector     ``Selector(memory=False)``
    ReactiveSequence     ``Sequence(memory=False)``
    Inverter             ``decorators.Inverter``
    ConditionNode        ``FluentCondition``
    ActionNode           ``ExecuteAction``
    SuccessLeaf          ``GoalReached``
    FailureLeaf          ``AlwaysFailure``
    ForbiddenActionNode  ``AlwaysFailure`` (with marker name)
    ==================== ==========================================

    Note: pr2_to_bt's deduplication may share BTNode objects across
    parents.  Since py_trees requires each node to have exactly one
    parent, this function creates fresh py_trees nodes for every
    occurrence.
    """
    if isinstance(node, ReactiveSelector):
        composite = py_trees.composites.Selector(
            name=node.name, memory=False
        )
        for child in node.children:
            composite.add_child(convert_to_pytrees(child))
        return composite

    if isinstance(node, ReactiveSequence):
        composite = py_trees.composites.Sequence(
            name=node.name, memory=False
        )
        for child in node.children:
            composite.add_child(convert_to_pytrees(child))
        return composite

    if isinstance(node, Inverter):
        return py_trees.decorators.Inverter(
            name="Inverter",
            child=convert_to_pytrees(node.child),
        )

    if isinstance(node, ConditionNode):
        return FluentCondition(node.fluent)

    if isinstance(node, ActionNode):
        return ExecuteAction(node.action_name)

    if isinstance(node, SuccessLeaf):
        return GoalReached()

    if isinstance(node, ForbiddenActionNode):
        return AlwaysFailure(name=f"Forbid:{node.forbidden_action}")

    if isinstance(node, FailureLeaf):
        return AlwaysFailure(name=node.name)

    raise ValueError(f"Unknown pr2_to_bt node type: {type(node).__name__}")


# ===================================================================
#  PDDL Grounding → World Factory
# ===================================================================


def build_simulator(domain_pddl: str, problem_pddl: str):
    """Build a BT simulator from raw PDDL text.

    Grounds the domain/problem with ``fondparser`` to extract initial
    state, goal conditions, and all action effects.

    Returns
    -------
    tuple of (make_world, action_table, goal_pos, goal_neg, init_fluents)
        ``make_world()`` → fresh ``SimulatedWorld`` at the initial state.
        ``action_table``  → ``{name: GroundedActionInfo}`` for all
        grounded operators.
    """
    tmpdir = None
    try:
        problem = ground_pddl(domain_pddl, problem_pddl)
    finally:
        pass  # cleanup handled by ground_pddl

    def _lower_set(s):
        return frozenset(f.lower() for f in s)

    # -- initial state -------------------------------------------------
    init_fluents: Set[str] = set()
    if isinstance(problem.init, And):
        for arg in problem.init.args:
            if isinstance(arg, Primitive):
                init_fluents.add(
                    _fondparser_predicate_to_fluent(arg.predicate).lower()
                )
    elif isinstance(problem.init, Primitive):
        init_fluents.add(
            _fondparser_predicate_to_fluent(problem.init.predicate).lower()
        )

    # -- goal ----------------------------------------------------------
    goal_pos_raw, goal_neg_raw = _extract_goal(problem.goal)
    goal_pos = _lower_set(goal_pos_raw)
    goal_neg = _lower_set(goal_neg_raw)

    # -- action table --------------------------------------------------
    action_table: Dict[str, GroundedAction] = {}
    for op in problem.operators:
        pr2_name = _fondparser_op_to_pr2_name(op).lower()
        pos_pre, neg_pre = _extract_preconditions(op.precondition)
        outcomes = _extract_outcomes(op)
        action_table[pr2_name] = GroundedAction(
            name=pr2_name,
            preconditions=_lower_set(pos_pre),
            neg_preconditions=_lower_set(neg_pre),
            outcomes=[(_lower_set(a), _lower_set(d)) for a, d in outcomes],
        )

    def make_world() -> SimulatedWorld:
        return SimulatedWorld(
            initial_fluents=set(init_fluents),
            action_table=action_table,
            goal_pos=goal_pos,
            goal_neg=goal_neg,
        )

    return make_world, action_table, goal_pos, goal_neg, init_fluents


