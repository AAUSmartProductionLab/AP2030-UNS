@dataclass
class SimulationResult:
    """Outcome of a Monte-Carlo BT simulation."""

    successes: int
    failures: int
    timeouts: int
    n_trials: int
    tick_counts: List[int]

    @property
    def success_rate(self) -> float:
        return self.successes / self.n_trials if self.n_trials else 0.0

    @property
    def avg_ticks(self) -> float:
        return (
            sum(self.tick_counts) / len(self.tick_counts)
            if self.tick_counts
            else 0.0
        )

    def summary(self) -> str:
        pct = self.success_rate * 100
        return (
            f"Success: {self.successes}/{self.n_trials} ({pct:.1f}%)  "
            f"Avg ticks: {self.avg_ticks:.1f}  "
            f"Failures: {self.failures}  Timeouts: {self.timeouts}"
        )


def run_simulation(
    bt: BehaviorTree,
    domain_pddl: str,
    problem_pddl: str,
    n_trials: int = 500,
    max_ticks: int = 5000,
    seed: Optional[int] = None,
    on_episode: Optional[Callable[[int, bool, int], None]] = None,
) -> SimulationResult:
    """Run Monte-Carlo BT simulation using py_trees.

    Converts the ``pr2_to_bt`` behaviour tree into a ``py_trees``
    tree, then runs *n_trials* independent episodes.  Each episode
    creates a fresh ``SimulatedWorld`` from the grounded PDDL and
    ticks the tree until the goal is reached, the tree fails, or
    *max_ticks* is exceeded.

    Parameters
    ----------
    bt : BehaviorTree
        The pr2_to_bt behaviour tree to simulate.
    domain_pddl, problem_pddl : str
        Raw PDDL text for world grounding.
    n_trials : int
        Number of independent simulation episodes.
    max_ticks : int
        Maximum ticks per episode before declaring timeout.
    seed : int, optional
        Random seed for reproducibility.
    on_episode : callable, optional
        ``(episode_idx, success, ticks)`` callback after each episode.

    Returns
    -------
    SimulationResult
        Aggregated statistics over all episodes.
    """
    if seed is not None:
        random.seed(seed)

    make_world, _, _, _, _ = build_simulator(domain_pddl, problem_pddl)

    # Convert pr2_to_bt tree → py_trees (once)
    pytree_root = convert_to_pytrees(bt.root)
    tree = py_trees.trees.BehaviourTree(root=pytree_root)
    tree.setup()

    # Blackboard writer for injecting world state
    writer = py_trees.blackboard.Client(name="Simulator")
    writer.register_key(
        key=_WORLD_KEY, access=py_trees.common.Access.WRITE
    )

    successes = 0
    failures = 0
    timeouts = 0
    tick_counts: List[int] = []

    for trial in range(n_trials):
        world = make_world()
        writer.set(_WORLD_KEY, world)

        episode_success = False
        episode_ticks = 0

        for tick in range(1, max_ticks + 1):
            tree.tick()
            episode_ticks = tick

            if world.goal_reached or world.check_goal():
                successes += 1
                tick_counts.append(tick)
                episode_success = True
                break

            if pytree_root.status == py_trees.common.Status.FAILURE:
                failures += 1
                break
        else:
            timeouts += 1

        if on_episode is not None:
            on_episode(trial, episode_success, episode_ticks)

    return SimulationResult(
        successes=successes,
        failures=failures,
        timeouts=timeouts,
        n_trials=n_trials,
        tick_counts=tick_counts,
    )


# ===================================================================
#  Display Utilities
# ===================================================================


def display_pytree(bt: BehaviorTree) -> str:
    """Convert a ``pr2_to_bt`` BT to py_trees and return its ASCII art.

    Uses ``py_trees.display.unicode_tree`` for a compact rendering
    of the tree structure.
    """
    pytree_root = convert_to_pytrees(bt.root)
    return py_trees.display.unicode_tree(root=pytree_root)


def render_pytree_dot(
    bt: BehaviorTree,
    output_path: str,
    with_blackboard_variables: bool = False,
) -> None:
    """Render the py_trees tree to a dot/svg/png file.

    Requires ``graphviz`` to be installed on the system.
    """
    pytree_root = convert_to_pytrees(bt.root)
    py_trees.display.render_dot_tree(
        pytree_root,
        target_directory=os.path.dirname(output_path) or ".",
        name=os.path.splitext(os.path.basename(output_path))[0],
        with_blackboard_variables=with_blackboard_variables,
    )
